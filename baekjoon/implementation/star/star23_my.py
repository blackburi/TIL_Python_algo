n = int(input())

for i in range(1, 2*n) :
    if i == 1 or i == (2*n-1) :
        print('*'*n + ' '*(2*n-3) + '*'*n)
    elif i < n :
        print(' '*(i-1) + '*' + ' '*(n-2) + '*' + ' '*(2*n-2*i-1) + '*' + ' '*(n-2) + '*')
    elif i == n :
        print(' '*(i-1) + '*' + ' '*(i-2) + '*' + ' '*(i-2) + '*')
    else :
        j = 2*n-i
        print(' '*(j-1) + '*' + ' '*(n-2) + '*' + ' '*(2*n-2*j-1) + '*' + ' '*(n-2) + '*')